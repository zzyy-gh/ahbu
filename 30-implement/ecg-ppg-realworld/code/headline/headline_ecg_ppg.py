def write_checklist(args, runs_dir: Path) -> dict:
    """Auto-generate the pre-run-checklist by inspecting expected artifacts.
    Each item is checked against the filesystem; if any item fails, the
    checklist file is still written (with FAIL markers) but `test` will
    refuse to run. Caller can hand-edit + flip to ALL_CHECKS_PASSED only
    if the auto-check is wrong (which would itself be a bug to fix).
    """
    out_dir = runs_dir / "headline"
    seeds = args.seeds
    checks = []

    # 1. Per-seed weights present.
    for s in seeds:
        p = out_dir / f"weights_seed{s}_best.pt"
        checks.append(("weights_seed%s_best.pt" % s, p.exists()))

    # 2. T fitted + saved per seed.
    for s in seeds:
        p = out_dir / f"calibration_seed{s}.json"
        checks.append(("calibration_seed%s.json" % s, p.exists()))

    # 3. Threshold present (same JSON as #2; double-check abstention_threshold key).
    for s in seeds:
        p = out_dir / f"calibration_seed{s}.json"
        if p.exists():
            try:
                d = json.loads(p.read_text())
                checks.append(("calibration_seed%s.json has abstention_threshold" % s, "abstention_threshold" in d))
            except Exception:  # noqa: BLE001
                checks.append(("calibration_seed%s.json parseable" % s, False))
        else:
            checks.append(("calibration_seed%s.json has abstention_threshold" % s, False))

    # 4. Partition audit.
    partition_audit = (runs_dir / "partition_audit.txt")
    checks.append(("partition_audit.txt exists", partition_audit.exists()))

    # 5. Test split untouched -- we cannot prove this from disk alone;
    # check that no earlier `test_seed*.json` exists.
    for s in seeds:
        p = out_dir / f"test_seed{s}.json"
        checks.append(("test_seed%s.json absent (not yet touched)" % s, not p.exists()))

    # 6. Seeds documented.
    checks.append(("seeds documented", len(seeds) > 0))
    checks.append(("batch_size documented", args.batch_size > 0))
    checks.append(("model variant documented", args.model in ("xresnet1d50", "xresnet1d18")))

    all_pass = all(ok for _, ok in checks)
    out = runs_dir / "pre-run-checklist.txt"
    lines = [
        "# Pre-run checklist — ecg-ppg-realworld headline",
        f"# Generated: {time.strftime('%Y-%m-%d %H:%M:%S')}",
        f"# Seeds: {seeds}",
        f"# Model: {args.model}",
        f"# Batch size: {args.batch_size}",
        "",
    ]
    for name, ok in checks:
        lines.append(f"[{'X' if ok else ' '}] {name}")
    lines.append("")
    lines.append("STATUS: ALL_CHECKS_PASSED" if all_pass else "STATUS: FAIL")
    out.write_text("\n".join(lines))
    print("\n".join(lines))
    return {"status": "pass" if all_pass else "fail", "checklist_path": str(out)}


def bootstrap_ppv_ci(y_true_bin, y_pred_bin, mask_committed, n_boot: int, seed: int):
    """Stratified-by-y_true bootstrap of PPV on the predicted-positive
    subset within the committed subset. Returns (point, lo, hi)."""
    import numpy as np

    rng = np.random.default_rng(seed)
    y_true_bin = np.asarray(y_true_bin)
    y_pred_bin = np.asarray(y_pred_bin)
    mask_committed = np.asarray(mask_committed).astype(bool)

    pos_idx = np.where(y_true_bin == 1)[0]
    neg_idx = np.where(y_true_bin == 0)[0]

    def _ppv(idx_pool):
        m = mask_committed[idx_pool]
        if m.sum() == 0:
            return float("nan")
        idx_committed = idx_pool[m]
        pp = y_pred_bin[idx_committed] == 1
        if pp.sum() == 0:
            return float("nan")
        return float((y_true_bin[idx_committed][pp] == 1).sum() / pp.sum())

    boots = []
    for _ in range(n_boot):
        s_pos = rng.choice(pos_idx, size=len(pos_idx), replace=True)
        s_neg = rng.choice(neg_idx, size=len(neg_idx), replace=True)
        s_all = np.concatenate([s_pos, s_neg])
        boots.append(_ppv(s_all))
    boots = np.asarray([b for b in boots if not (b != b)], dtype=np.float64)
    point = _ppv(np.arange(len(y_true_bin)))
    if len(boots) == 0:
        return point, float("nan"), float("nan")
    lo = float(np.quantile(boots, 0.025))
    hi = float(np.quantile(boots, 0.975))
    return point, lo, hi


def compute_ece(probs, y_true, n_bins: int = 15) -> float:
    import numpy as np

    pred = probs.argmax(axis=1)
    conf = probs.max(axis=1)
    correct = (pred == y_true).astype(float)
    bins = np.linspace(0.0, 1.0, n_bins + 1)
    ece = 0.0
    n = len(y_true)
    for i in range(n_bins):
        lo, hi = bins[i], bins[i + 1]
        if i == n_bins - 1:
            mask = (conf >= lo) & (conf <= hi)
        else:
            mask = (conf >= lo) & (conf < hi)
        if mask.sum() == 0:
            continue
        acc = float(correct[mask].mean())
        c = float(conf[mask].mean())
        ece += (mask.sum() / n) * abs(acc - c)
    return float(ece)


def run_test(args):
    """Single authorized touch of the held-out partition.

    Refuses to proceed unless `runs/pre-run-checklist.txt` ends in
    `STATUS: ALL_CHECKS_PASSED`. Caller must run --phase=write-checklist
    first.
    """
    import numpy as np
    import pandas as pd
    import torch
    from scipy.special import softmax
    from sklearn.metrics import roc_auc_score
    from statsmodels.stats.contingency_tables import mcnemar

    from shared.eval import ppv_at_coverage, selective_classification_curve

    cinc_dir = args.cinc_dir
    partition_json = args.partition_json
    runs_dir = args.runs_dir
    seed = args.seed
    out_dir = runs_dir / "headline"
    weights_path = out_dir / f"weights_seed{seed}_best.pt"
    cal_path = out_dir / f"calibration_seed{seed}.json"
    test_out_path = out_dir / f"test_seed{seed}.json"

    # Idempotency guard: if test_seed{seed}.json already exists, refuse
    # to overwrite. Held-out is touched once.
    if test_out_path.exists():
        return {"status": "fail", "reason": f"test_seed{seed}.json already exists. Held-out has already been touched for this seed. Move/rename the existing file if a re-run is genuinely necessary (and document why)."}

    checklist = runs_dir / "pre-run-checklist.txt"
    if not checklist.exists():
        return {"status": "fail", "reason": f"checklist missing at {checklist}. Run --phase=write-checklist first."}
    if "ALL_CHECKS_PASSED" not in checklist.read_text():
        return {"status": "fail", "reason": "checklist STATUS != ALL_CHECKS_PASSED"}

    if not weights_path.exists():
        return {"status": "fail", "reason": f"weights not found: {weights_path}"}
    if not cal_path.exists():
        return {"status": "fail", "reason": f"calibration not found: {cal_path}"}
    cal_meta = json.loads(cal_path.read_text())
    T = float(cal_meta["T"])
    threshold = float(cal_meta["abstention_threshold"])

    if not torch.cuda.is_available():
        return {"status": "fail", "reason": "torch.cuda unavailable"}

    reference_csv = find_reference_csv(cinc_dir)
    waveform_dir = reference_csv.parent
    df = pd.read_csv(reference_csv, header=None, names=["record_id", "label"])
    partition = json.loads(partition_json.read_text())
    test_ids = set(partition["test_record_ids"])
    df_test = df[df["record_id"].isin(test_ids)].copy()
    # Spec §2: 46 noisy '~' excluded throughout (training + evaluation).
    # Per partition_audit they should not appear in test_record_ids since
    # the working set excludes noisy. But filter defensively in case.
    n_pre = len(df_test)
    df_test = df_test[df_test["label"] != "~"].reset_index(drop=True)
    n_excluded_noisy = n_pre - len(df_test)

    print(f"[test] HELD-OUT TOUCH: seed={seed} test_records={len(df_test)} (excluded_noisy={n_excluded_noisy}) T={T:.4f} threshold={threshold:.4f}", flush=True)

    print("[test] loading test waveforms...", flush=True)
    t0 = time.perf_counter()
    X_test, y_test, n_failed = in_memory_load(df_test["record_id"].values, df_test["label"].values, waveform_dir, args.target_len, args.fs)
    print(f"[test] load done in {time.perf_counter()-t0:.1f}s (failed={n_failed})", flush=True)

    device = "cuda:0"
    model = build_xresnet1d50(in_channels=1, n_out=N_CLASSES).to(device)
    state = torch.load(str(weights_path), map_location=device, weights_only=False)
    if isinstance(state, dict) and "state_dict" in state:
        model.load_state_dict(state["state_dict"])
    else:
        model.load_state_dict(state)
    model.eval()

    print("[test] inference...", flush=True)
    logits = []
    with torch.no_grad():
        for s in range(0, len(X_test), args.batch_size):
            xb = torch.from_numpy(X_test[s : s + args.batch_size]).unsqueeze(1).to(device)
            logits.append(model(xb).cpu().numpy())
    logits = np.concatenate(logits, axis=0)
    probs_uncal = softmax(logits, axis=1)
    probs_cal = softmax(logits / max(T, 1e-3), axis=1)

    pred = probs_cal.argmax(axis=1)
    conf = probs_cal.max(axis=1)
    committed = conf >= threshold
    achieved_abstention = float(1.0 - committed.mean())

    # AUROC AF.
    y_bin = (y_test == AF_CLASS).astype(np.int64)
    auroc_af = float(roc_auc_score(y_bin, probs_cal[:, AF_CLASS])) if y_bin.sum() > 0 else float("nan")

    # ECE pre/post T (overall).
    ece_uncal = compute_ece(probs_uncal, y_test, n_bins=15)
    ece_cal = compute_ece(probs_cal, y_test, n_bins=15)

    # PPV at 17% (committed subset, AF-vs-rest).
    pred_bin = (pred == AF_CLASS).astype(np.int64)
    ppv_17_point, ppv_17_lo, ppv_17_hi = bootstrap_ppv_ci(y_bin, pred_bin, committed, n_boot=args.n_boot, seed=42)
    n_committed = int(committed.sum())

    # Baseline PPV at 0% abstention.
    all_in = np.ones_like(committed, dtype=bool)
    ppv_0_point, ppv_0_lo, ppv_0_hi = bootstrap_ppv_ci(y_bin, pred_bin, all_in, n_boot=args.n_boot, seed=42)

    # PPV-vs-coverage curve at 6 protocol-locked points.
    coverages = [1.00, 0.95, 0.90, 0.85, 0.83, 0.79]
    sc = selective_classification_curve(
        confidences=conf,
        correct=(pred == y_test).astype(np.int64),
        coverages=coverages,
    )
    ppv_curve = ppv_at_coverage(
        y_true=y_bin,
        y_pred=pred_bin,
        confidences=conf,
        coverages=coverages,
        bootstrap_n=args.n_boot,
        bootstrap_seed=42,
    )

    # McNemar (PPV-flavoured): on AF-predicted-positive subset under
    # both conditions. The "no-abstention" condition commits on every
    # input (mask = all-True). The "abstention" condition uses the
    # threshold mask. We compare TP/FP labels for AF-predicted records
    # that survive in BOTH conditions.
    af_pred_idx = np.where(pred_bin == 1)[0]
    # Both conditions must commit. With threshold-based abstention,
    # the abstention condition drops some AF-predicted records (those
    # with conf < threshold). Define the comparison universe as the
    # union of AF-predicted records under either condition; then label
    # each as TP/FP per condition (records dropped by abstention are
    # excluded from the abstention column entirely).
    table = np.array([
        # Rows: no-abstain TP / FP. Columns: abstention TP / FP / dropped.
        # Standard McNemar 2x2: discordant cells only matter.
        # We construct a 2x2 by counting:
        #   a = both TP (concordant TP)
        #   b = no-abstain TP, abstention drops or FP
        #   c = no-abstain FP, abstention TP
        #   d = both FP / both dropped (concordant)
        [0, 0],
        [0, 0],
    ], dtype=np.int64)
    for i in af_pred_idx:
        no_abs_tp = (y_bin[i] == 1)
        if not committed[i]:
            # abstention dropped this AF-predicted record entirely.
            # In a PPV-comparing 2x2, dropping an FP is good for PPV,
            # dropping a TP is bad for PPV. Treat drops as the
            # "discordant" cells:
            #   no-abstain TP, abstention drops (loss of TP) -> b cell
            #   no-abstain FP, abstention drops (gain for PPV) -> c cell
            if no_abs_tp:
                table[0, 1] += 1  # b
            else:
                table[1, 0] += 1  # c
        else:
            abs_tp = (y_bin[i] == 1)
            if no_abs_tp and abs_tp:
                table[0, 0] += 1  # a
            elif (not no_abs_tp) and (not abs_tp):
                table[1, 1] += 1  # d
            elif no_abs_tp and not abs_tp:
                table[0, 1] += 1  # discordant TP->FP: b
            else:
                table[1, 0] += 1  # discordant FP->TP: c
    # statsmodels McNemar uses table where off-diagonals are b and c.
    try:
        m_res = mcnemar(table, exact=False, correction=True)
        mcnemar_p = float(m_res.pvalue)
        mcnemar_stat = float(m_res.statistic)
    except Exception as e:  # noqa: BLE001
        mcnemar_p = float("nan")
        mcnemar_stat = float("nan")
        print(f"[test] McNemar fallback: {e}", flush=True)

    ppv_improvement_pp = (ppv_17_point - ppv_0_point) * 100.0

    # Verdict per protocol-lock §6 / §7.
    if (mcnemar_p < 0.05) and (ppv_improvement_pp >= 5.0):
        verdict = "POSITIVE: statistically significant and clinically meaningful PPV improvement from calibrated abstention"
    elif (mcnemar_p < 0.05) and (ppv_improvement_pp < 5.0):
        verdict = "POSITIVE-SMALL: statistically significant but small PPV improvement"
    elif (mcnemar_p >= 0.05) and (ppv_17_hi - ppv_0_point) * 100.0 < 10.0:
        verdict = "NULL: no statistically significant PPV improvement; CI rules out 10pp+"
    elif mcnemar_p >= 0.05:
        verdict = "INCONCLUSIVE: no significance and CI too wide to rule out a meaningful effect"
    else:
        verdict = "UNCATEGORISED"

    result = {
        "status": "pass",
        "seed": seed,
        "T": round(T, 5),
        "abstention_threshold": round(threshold, 6),
        "abstention_threshold_target": cal_meta["abstention_threshold_target_rate"],
        "abstention_rate_achieved_on_test": round(achieved_abstention, 5),
        "n_test": int(len(y_test)),
        "n_excluded_noisy": int(n_excluded_noisy),
        "n_committed": n_committed,
        "auroc_af_post_T": round(auroc_af, 5),
        "ece_uncal": round(ece_uncal, 5),
        "ece_cal": round(ece_cal, 5),
        "ppv_at_17_abstention": {
            "point": round(ppv_17_point, 5),
            "ci95_lo": round(ppv_17_lo, 5),
            "ci95_hi": round(ppv_17_hi, 5),
            "n_committed": n_committed,
        },
        "ppv_at_0_abstention_baseline": {
            "point": round(ppv_0_point, 5),
            "ci95_lo": round(ppv_0_lo, 5),
            "ci95_hi": round(ppv_0_hi, 5),
            "n_committed": int(len(y_test)),
        },
        "ppv_improvement_pp": round(ppv_improvement_pp, 3),
        "mcnemar_table_2x2": table.tolist(),
        "mcnemar_statistic": round(mcnemar_stat, 5),
        "mcnemar_p_value": round(mcnemar_p, 5),
        "ppv_vs_coverage_curve": ppv_curve,
        "selective_classification_curve": sc,
        "bootstrap_n": args.n_boot,
        "bootstrap_seed": 42,
        "weights_path": str(weights_path),
        "verdict": verdict,
        "spec": "20-plan/ecg-ppg-realworld/protocol-lock.md §5-§7",
    }
    test_out_path.write_text(json.dumps(result, indent=2))
    print(json.dumps({k: v for k, v in result.items() if k not in ("ppv_vs_coverage_curve", "selective_classification_curve")}, indent=2))
    print(f"\nWrote {test_out_path}", flush=True)
    return result


