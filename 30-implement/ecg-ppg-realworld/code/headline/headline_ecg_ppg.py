def class_weights_inv_freq(y, n_classes: int):
    import numpy as np

    counts = np.bincount(y, minlength=n_classes).astype(np.float32)
    total = counts.sum()
    # Avoid div-by-zero.
    counts = np.where(counts == 0, 1.0, counts)
    weights = total / (n_classes * counts)
    return weights


def af_auroc(y_true, probs):
    import numpy as np
    from sklearn.metrics import roc_auc_score

    y_bin = (np.asarray(y_true) == AF_CLASS).astype(np.int64)
    if y_bin.sum() == 0 or y_bin.sum() == len(y_bin):
        return float("nan")
    return float(roc_auc_score(y_bin, probs[:, AF_CLASS]))


def run_train(args):
    import numpy as np
    import pandas as pd
    import torch
    import torch.nn as nn
    from scipy.special import softmax
    from sklearn.model_selection import StratifiedShuffleSplit
    from torch.optim import Adam
    from torch.optim.lr_scheduler import CosineAnnealingLR

    cinc_dir = args.cinc_dir
    partition_json = args.partition_json
    runs_dir = args.runs_dir
    seed = args.seed

    if not cinc_dir.exists():
        return {"status": "fail", "reason": f"CinC 2017 dir not found at {cinc_dir}"}
    if not partition_json.exists():
        return {"status": "fail", "reason": f"partition.json not found at {partition_json}"}

    reference_csv = find_reference_csv(cinc_dir)
    if reference_csv is None:
        return {"status": "fail", "reason": f"REFERENCE csv not found under {cinc_dir}"}
    if not torch.cuda.is_available():
        return {"status": "fail", "reason": "torch.cuda unavailable"}

    waveform_dir = reference_csv.parent
    df = pd.read_csv(reference_csv, header=None, names=["record_id", "label"])
    partition = json.loads(partition_json.read_text())
    dev_ids = set(partition["dev_record_ids"])
    df_dev = df[df["record_id"].isin(dev_ids)].copy()
    # Spec excludes noisy '~' from training (carried through from P-2 / P-4).
    df_dev = df_dev[df_dev["label"] != "~"].reset_index(drop=True)

    # Internal dev split: 20% calibration hold-out (used in `calibrate`
    # phase), 80% training. Same seed/stratification as P-4 to keep the
    # training set stable across phases.
    sss = StratifiedShuffleSplit(n_splits=1, test_size=0.20, random_state=42)
    train_idx, cal_holdout_idx = next(sss.split(df_dev["record_id"].values, df_dev["label"].values))
    df_train = df_dev.iloc[train_idx].reset_index(drop=True)
    df_cal_holdout = df_dev.iloc[cal_holdout_idx].reset_index(drop=True)

    print(f"[headline-train] seed={seed} train={len(df_train)} cal_holdout={len(df_cal_holdout)}", flush=True)

    print("[headline-train] loading + preprocessing waveforms...", flush=True)
    t0 = time.perf_counter()
    X_train, y_train, n_failed_train = in_memory_load(
        df_train["record_id"].values, df_train["label"].values, waveform_dir, args.target_len, args.fs
    )
    X_cal, y_cal, n_failed_cal = in_memory_load(
        df_cal_holdout["record_id"].values, df_cal_holdout["label"].values, waveform_dir, args.target_len, args.fs
    )
    load_elapsed = time.perf_counter() - t0
    print(f"[headline-train] load complete in {load_elapsed:.1f}s (train_failed={n_failed_train}, cal_failed={n_failed_cal})", flush=True)

    cw = class_weights_inv_freq(y_train, N_CLASSES)
    print(f"[headline-train] class weights (inv-freq): {cw.tolist()}", flush=True)

    # Reproducibility
    torch.manual_seed(seed)
    np.random.seed(seed)
    rng = np.random.default_rng(seed)

    device = "cuda:0"
    model = build_xresnet1d50(in_channels=1, n_out=N_CLASSES).to(device)
    weight_tensor = torch.tensor(cw, dtype=torch.float32, device=device)
    criterion = nn.CrossEntropyLoss(weight=weight_tensor)
    optimizer = Adam(model.parameters(), lr=args.lr)
    scheduler = CosineAnnealingLR(optimizer, T_max=args.epochs)

    out_dir = runs_dir / "headline"
    out_dir.mkdir(parents=True, exist_ok=True)
    history = []
    best_auc = -1.0
    best_path = out_dir / f"weights_seed{seed}_best.pt"
    epochs_since_best = 0
    early_stop_triggered = False

    n_train = len(X_train)
    train_idx_arr = np.arange(n_train)
    for epoch in range(1, args.epochs + 1):
        model.train()
        rng.shuffle(train_idx_arr)
        running = 0.0
        n_seen = 0
        ep_t0 = time.perf_counter()
        for s in range(0, n_train, args.batch_size):
            batch_idx = train_idx_arr[s : s + args.batch_size]
            xb = torch.from_numpy(X_train[batch_idx]).unsqueeze(1).to(device, non_blocking=True)
            yb = torch.from_numpy(y_train[batch_idx]).to(device, non_blocking=True)
            optimizer.zero_grad(set_to_none=True)
            logits = model(xb)
            loss = criterion(logits, yb)
            loss.backward()
            optimizer.step()
            running += float(loss.item()) * len(batch_idx)
            n_seen += len(batch_idx)
            if (s // args.batch_size) % 100 == 99:
                print(f"  [seed={seed} ep {epoch}/{args.epochs}] step {s//args.batch_size+1}/{(n_train+args.batch_size-1)//args.batch_size} running_loss={running/n_seen:.4f} elapsed={time.perf_counter()-ep_t0:.0f}s", flush=True)
        scheduler.step()
        train_loss = running / max(n_seen, 1)

        # Eval on calibration hold-out for early stopping monitor.
        model.eval()
        cal_logits = []
        with torch.no_grad():
            for s in range(0, len(X_cal), args.batch_size):
                xb = torch.from_numpy(X_cal[s : s + args.batch_size]).unsqueeze(1).to(device)
                cal_logits.append(model(xb).cpu().numpy())
        cal_logits = np.concatenate(cal_logits, axis=0)
        cal_probs = softmax(cal_logits, axis=1)
        cal_auc = af_auroc(y_cal, cal_probs)

        ep_elapsed = time.perf_counter() - ep_t0
        record = {"epoch": epoch, "train_loss": round(train_loss, 5), "cal_holdout_af_auroc": round(cal_auc, 5), "elapsed_s": round(ep_elapsed, 1), "lr": float(scheduler.get_last_lr()[0])}
        history.append(record)
        print(f"[seed={seed}] epoch {epoch}: train_loss={train_loss:.4f} cal_af_auroc={cal_auc:.4f} ({ep_elapsed:.0f}s)", flush=True)

        # Per-epoch checkpoint (overwrite — no need to keep all).
        torch.save(model.state_dict(), out_dir / f"weights_seed{seed}_last.pt")
        # Early-stopping bookkeeping.
        if cal_auc > best_auc:
            best_auc = cal_auc
            epochs_since_best = 0
            torch.save(model.state_dict(), best_path)
            print(f"  -> new best @ epoch {epoch}: AUROC={best_auc:.4f}", flush=True)
        else:
            epochs_since_best += 1
            if epochs_since_best >= args.early_stop_patience:
                early_stop_triggered = True
                print(f"  -> early stop at epoch {epoch} (no improvement in {args.early_stop_patience} epochs; best={best_auc:.4f})", flush=True)
                break

    history_path = out_dir / f"train_seed{seed}_history.json"
    history_path.write_text(json.dumps({
        "seed": seed,
        "epochs_run": len(history),
        "epochs_planned": args.epochs,
        "early_stop_triggered": early_stop_triggered,
        "best_cal_holdout_af_auroc": round(best_auc, 5),
        "history": history,
        "best_weights": str(best_path),
    }, indent=2))
    print(f"[headline-train] seed={seed} done. best AUROC={best_auc:.4f}. History: {history_path}", flush=True)
    return {"status": "pass", "seed": seed, "best_auroc": round(best_auc, 5), "epochs_run": len(history), "best_weights_path": str(best_path)}


