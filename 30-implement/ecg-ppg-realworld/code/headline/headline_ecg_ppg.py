"""
Spec: 20-plan/ecg-ppg-realworld/protocol-lock.md

Headline script — ecg-ppg-realworld.

Phases (selected via --phase):
  train     : train xresnet1d50 on dev split for `--epochs` (default 50)
              with early-stopping (patience 10 on dev-AF AUROC), saving
              best-AUROC weights per seed. Repeats for each --seed.
              Dev-only.
  calibrate : fit temperature T on dev calibration hold-out
              (20% of dev, seed=42 stratified) by Brier-min; fit
              abstention threshold on the remaining 80% of dev to
              achieve 17% abstention rate. Saves T + threshold per
              seed. Dev-only.
  test      : SINGLE-USE — apply T + threshold to held-out test split
              (record IDs from runs/partition.json["test_record_ids"]).
              Compute primary metric (PPV @ 17% abstention with bootstrap
              CI), baseline (PPV @ 0%), AUROC AF, ECE pre/post T,
              PPV-vs-coverage curve, McNemar test (PPV-flavoured) on the
              AF-predicted-positive subset, and multi-seed stability if
              multiple seeds are given. Touches held-out exactly once.
  all       : convenience: run train -> calibrate -> test for each seed.

Pre-run checklist (per protocol-lock §7) must be confirmed before
`test` will execute. The script writes a checklist log to
runs/pre-run-checklist.txt and refuses to run `test` unless the
checklist file exists with status "ALL_CHECKS_PASSED" (manual or
auto-generated by `--write-checklist`).

Outputs (all under 30-implement/ecg-ppg-realworld/runs/headline/):
  - weights_seed{seed}_epoch{n}.pt        (training checkpoints)
  - weights_seed{seed}_best.pt            (best by dev-AF AUROC)
  - train_seed{seed}_history.json         (per-epoch metrics)
  - calibration_seed{seed}.json           (T, threshold, dev metrics)
  - test_seed{seed}.json                  (held-out metrics; per seed)
  - headline_summary.json                 (cross-seed aggregate)
  - pre-run-checklist.txt
"""

