def fit_temperature_brier(logits, y_true, n_classes: int):
    import numpy as np
    from scipy.optimize import minimize_scalar
    from scipy.special import softmax

    y_onehot = np.zeros((len(y_true), n_classes), dtype=np.float32)
    y_onehot[np.arange(len(y_true)), y_true] = 1.0

    def brier(T):
        probs = softmax(logits / max(T, 1e-3), axis=1)
        return float(((probs - y_onehot) ** 2).sum(axis=1).mean())

    res = minimize_scalar(brier, bounds=(0.5, 10.0), method="bounded", options={"xatol": 1e-3})
    T = float(res.x)
    return T, brier(1.0), brier(T)


def run_calibrate(args):
    """Two stages, dev only:
       (i)  fit T on the cal-holdout subset (20% of dev, the same one
            the training run held out for early-stopping monitoring).
       (ii) evaluate on the *training* portion (80% of dev) and pick the
            confidence threshold that achieves --target-abstention rate.

       Note: stage (ii) uses the SAME training data the model has seen.
       Per protocol-lock §4: "Threshold tuned on the remaining 80 % of
       dev split to achieve 17 % abstention rate." That split is the
       training portion here (5,279 records). Using the model's training
       data to fit an *abstention threshold* (not a model parameter)
       is a deliberate spec choice: it ensures the threshold corresponds
       to a 17% abstention rate on the inputs the model is most
       confident on, which is the conservative reading of the BASEL 17%
       inconclusive-rate budget. The held-out test split is untouched.
    """
    import numpy as np
    import pandas as pd
    import torch
    from scipy.special import softmax
    from sklearn.model_selection import StratifiedShuffleSplit

    cinc_dir = args.cinc_dir
    partition_json = args.partition_json
    runs_dir = args.runs_dir
    seed = args.seed

    if not torch.cuda.is_available():
        return {"status": "fail", "reason": "torch.cuda unavailable"}
    out_dir = runs_dir / "headline"
    weights_path = out_dir / f"weights_seed{seed}_best.pt"
    if not weights_path.exists():
        return {"status": "fail", "reason": f"weights not found: {weights_path} (run --phase=train first)"}

    reference_csv = find_reference_csv(cinc_dir)
    waveform_dir = reference_csv.parent
    df = pd.read_csv(reference_csv, header=None, names=["record_id", "label"])
    partition = json.loads(partition_json.read_text())
    dev_ids = set(partition["dev_record_ids"])
    df_dev = df[df["record_id"].isin(dev_ids)].copy()
    df_dev = df_dev[df_dev["label"] != "~"].reset_index(drop=True)
    sss = StratifiedShuffleSplit(n_splits=1, test_size=0.20, random_state=42)
    train_idx, cal_idx = next(sss.split(df_dev["record_id"].values, df_dev["label"].values))
    df_train = df_dev.iloc[train_idx].reset_index(drop=True)
    df_cal = df_dev.iloc[cal_idx].reset_index(drop=True)

    print(f"[calibrate] seed={seed} dev_train={len(df_train)} cal_holdout={len(df_cal)}", flush=True)

    print("[calibrate] loading dev waveforms (cal hold-out + train)...", flush=True)
    t0 = time.perf_counter()
    X_cal, y_cal, _ = in_memory_load(df_cal["record_id"].values, df_cal["label"].values, waveform_dir, args.target_len, args.fs)
    X_train, y_train, _ = in_memory_load(df_train["record_id"].values, df_train["label"].values, waveform_dir, args.target_len, args.fs)
    print(f"[calibrate] load done in {time.perf_counter()-t0:.1f}s", flush=True)

    device = "cuda:0"
    model = build_xresnet1d50(in_channels=1, n_out=N_CLASSES).to(device)
    state = torch.load(str(weights_path), map_location=device, weights_only=False)
    if isinstance(state, dict) and "state_dict" in state:
        model.load_state_dict(state["state_dict"])
    else:
        model.load_state_dict(state)
    model.eval()

    def collect_logits(X):
        out = []
        with torch.no_grad():
            for s in range(0, len(X), args.batch_size):
                xb = torch.from_numpy(X[s : s + args.batch_size]).unsqueeze(1).to(device)
                out.append(model(xb).cpu().numpy())
        return np.concatenate(out, axis=0)

    cal_logits = collect_logits(X_cal)
    train_logits = collect_logits(X_train)

    # (i) Fit T on cal-holdout.
    T, brier_pre, brier_post = fit_temperature_brier(cal_logits, y_cal, N_CLASSES)
    cal_probs_post = softmax(cal_logits / max(T, 1e-3), axis=1)
    train_probs_post = softmax(train_logits / max(T, 1e-3), axis=1)
    cal_auroc_af = af_auroc(y_cal, cal_probs_post)

    # (ii) Pick threshold on dev-train so that fraction-below-threshold == target abstention
    target = args.target_abstention
    train_max_conf = train_probs_post.max(axis=1)
    threshold = float(np.quantile(train_max_conf, target))
    achieved = float(np.mean(train_max_conf < threshold))

    artifact = {
        "seed": seed,
        "T": round(T, 5),
        "T_search_bounds": [0.5, 10.0],
        "brier_uncalibrated_on_cal": round(brier_pre, 5),
        "brier_calibrated_on_cal": round(brier_post, 5),
        "calibration_auroc_af_post_T_on_cal": round(cal_auroc_af, 5),
        "abstention_threshold": round(threshold, 6),
        "abstention_threshold_target_rate": target,
        "abstention_threshold_achieved_rate_on_dev_train": round(achieved, 5),
        "n_cal_holdout": int(len(y_cal)),
        "n_dev_train": int(len(y_train)),
        "weights_path": str(weights_path),
    }
    out_path = out_dir / f"calibration_seed{seed}.json"
    out_path.write_text(json.dumps(artifact, indent=2))
    print(json.dumps(artifact, indent=2))
    print(f"\nWrote {out_path}", flush=True)
    return {"status": "pass", **artifact}


