def run_aggregate(args):
    import numpy as np

    out_dir = args.runs_dir / "headline"
    seed_results = []
    for s in args.seeds:
        p = out_dir / f"test_seed{s}.json"
        if p.exists():
            seed_results.append(json.loads(p.read_text()))
    if not seed_results:
        return {"status": "fail", "reason": "no test_seed*.json found yet"}

    def _val(key, sub=None):
        out = []
        for r in seed_results:
            v = r[key] if sub is None else r[key][sub]
            out.append(v)
        return np.asarray(out, dtype=float)

    summary = {
        "n_seeds_aggregated": len(seed_results),
        "seeds": [r["seed"] for r in seed_results],
        "ppv_at_17_abstention_mean": float(_val("ppv_at_17_abstention", "point").mean()),
        "ppv_at_17_abstention_std": float(_val("ppv_at_17_abstention", "point").std(ddof=1) if len(seed_results) > 1 else 0.0),
        "ppv_baseline_mean": float(_val("ppv_at_0_abstention_baseline", "point").mean()),
        "ppv_baseline_std": float(_val("ppv_at_0_abstention_baseline", "point").std(ddof=1) if len(seed_results) > 1 else 0.0),
        "auroc_af_mean": float(_val("auroc_af_post_T").mean()),
        "auroc_af_std": float(_val("auroc_af_post_T").std(ddof=1) if len(seed_results) > 1 else 0.0),
        "abstention_rate_achieved_mean": float(_val("abstention_rate_achieved_on_test").mean()),
        "ece_cal_mean": float(_val("ece_cal").mean()),
        "primary_seed": 42,
        "primary_seed_result": next((r for r in seed_results if r["seed"] == 42), None),
    }
    out_path = out_dir / "headline_summary.json"
    out_path.write_text(json.dumps(summary, indent=2))
    print(json.dumps({k: v for k, v in summary.items() if k != "primary_seed_result"}, indent=2))
    print(f"\nWrote {out_path}")
    return {"status": "pass", **{k: v for k, v in summary.items() if k != "primary_seed_result"}}


