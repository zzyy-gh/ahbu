def preprocess(signal, fs: int):
    import numpy as np
    from scipy.signal import butter, filtfilt, iirnotch

    sos_b, sos_a = butter(4, 0.5 / (fs / 2), btype="highpass")
    signal = filtfilt(sos_b, sos_a, signal)
    notch_b, notch_a = iirnotch(50.0 / (fs / 2), Q=30)
    signal = filtfilt(notch_b, notch_a, signal)
    signal = np.clip(signal, -5.0, 5.0)
    mu = float(signal.mean())
    sigma = float(signal.std())
    if sigma > 1e-9:
        signal = (signal - mu) / sigma
    return signal


def find_reference_csv(cinc_dir: Path):
    for cand in (
        "REFERENCE-v3.csv",
        "REFERENCE.csv",
        "training2017/REFERENCE-v3.csv",
        "training2017/REFERENCE.csv",
    ):
        p = cinc_dir / cand
        if p.exists():
            return p
    return None


def build_xresnet1d50(in_channels: int = 1, n_out: int = 4):
    from fastai.vision.models.xresnet import ResBlock, XResNet

    return XResNet(
        ResBlock,
        expansion=4,
        layers=[3, 4, 6, 3],
        c_in=in_channels,
        n_out=n_out,
        ndim=1,
        ks=5,
    )


def pad_or_crop(signal, target_len: int):
    import numpy as np

    n = len(signal)
    if n == target_len:
        return signal.astype(np.float32)
    if n > target_len:
        start = (n - target_len) // 2
        return signal[start : start + target_len].astype(np.float32)
    out = np.zeros(target_len, dtype=np.float32)
    out[:n] = signal.astype(np.float32)
    return out


def load_record_signal(rec_path: Path, target_len: int, fs: int):
    import wfdb

    r = wfdb.rdrecord(str(rec_path))
    sig = r.p_signal[:, 0]
    sig = preprocess(sig, fs)
    sig = pad_or_crop(sig, target_len)
    return sig


LABEL_MAP = {"N": 0, "A": 1, "O": 2, "~": 3}
N_CLASSES = 4
AF_CLASS = 1


def in_memory_load(record_ids, labels, waveform_dir: Path, target_len: int, fs: int, log_every: int = 500):
    import numpy as np

    n = len(record_ids)
    X = np.zeros((n, target_len), dtype=np.float32)
    y = np.zeros(n, dtype=np.int64)
    n_failed = 0
    for i, (rid, lbl) in enumerate(zip(record_ids, labels)):
        try:
            X[i] = load_record_signal(waveform_dir / rid, target_len, fs)
        except Exception:  # noqa: BLE001
            n_failed += 1
        y[i] = LABEL_MAP[lbl]
        if (i + 1) % log_every == 0:
            print(f"  [load] {i+1}/{n} (failed={n_failed})", flush=True)
    return X, y, n_failed


